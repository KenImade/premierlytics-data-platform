with matches_base as (
    select * from {{ref('stg_matches')}}
),

home_side as (
    select
        match_id,
        season,
        gameweek,
        kickoff_time,
        match_url,
        fotmob_id,
        finished as is_finished,
        stats_processed as is_stats_processed,
        player_stats_processed as is_player_stats_processed,

        -- Team identity
        true as is_home,
        home_team as team_code,
        away_team as opponent_code,
        home_team_elo as team_elo,
        away_team_elo as opponent_elo,

        -- Home stats renamed
        home_score as score,
        away_score as opponent_score,
        home_possession as possession,
        home_expected_goals_xg as expected_goals_xg,
        home_xg_open_play as xg_open_play,
        home_xg_set_play as xg_set_play,
        home_non_penalty_xg as non_penalty_xg,
        home_xg_on_target_xgot as xg_on_target_xgot,
        home_total_shots as total_shots,
        home_shots_on_target as shots_on_target,
        home_shots_off_target as shots_off_target,
        home_blocked_shots as blocked_shots,
        home_shots_inside_box as shots_inside_box,
        home_shots_outside_box as shots_outside_box,
        home_big_chances as big_chances,
        home_big_chances_missed as big_chances_missed,
        home_hit_woodwork as hit_woodwork,
        home_passes as passes,
        home_accurate_passes as accurate_passes,
        home_accurate_passes_pct as accurate_passes_pct,
        home_own_half as own_half,
        home_opposition_half as opposition_half,
        home_accurate_long_balls as accurate_long_balls,
        home_accurate_crosses as accurate_crosses,
        home_accurate_crosses_pct as accurate_crosses_pct,
        home_corners as corners,
        home_throws as throws,
        home_fouls_committed as fouls_committed,
        home_offsides as offsides,
        home_yellow_cards as yellow_cards,
        home_red_cards as red_cards,
        home_touches_in_opposition_box as touches_in_opposition_box,
        home_tackles_won as tackles_won,
        home_tackles_won_pct as tackles_won_pct,
        home_interceptions as interceptions,
        home_blocks as blocks,
        home_clearances as clearances,
        home_keeper_saves as keeper_saves,
        home_duels_won as duels_won,
        home_ground_duels_won as ground_duels_won,
        home_ground_duels_won_pct as ground_duels_won_pct,
        home_aerial_duels_won as aerial_duels_won,
        home_aerial_duels_won_pct as aerial_duels_won_pct,
        home_successful_dribbles as successful_dribbles,
        home_successful_dribbles_pct as successful_dribbles_pct,
        ingested_at
    from matches_base
),

away_side as (
    select
        match_id,
        season,
        gameweek,
        kickoff_time,
        match_url,
        fotmob_id,
        finished as is_finished,
        stats_processed as is_stats_processed,
        player_stats_processed as is_player_stats_processed,

        -- Team identity
        false as is_home,
        away_team as team_code,
        home_team as opponent_code,
        away_team_elo as team_elo,
        home_team_elo as opponent_elo,

        -- Away stats renamed
        away_score as score,
        home_score as opponent_score,
        away_possession as possession,
        away_expected_goals_xg as expected_goals_xg,
        away_xg_open_play as xg_open_play,
        away_xg_set_play as xg_set_play,
        away_non_penalty_xg as non_penalty_xg,
        away_xg_on_target_xgot as xg_on_target_xgot,
        away_total_shots as total_shots,
        away_shots_on_target as shots_on_target,
        away_shots_off_target as shots_off_target,
        away_blocked_shots as blocked_shots,
        away_shots_inside_box as shots_inside_box,
        away_shots_outside_box as shots_outside_box,
        away_big_chances as big_chances,
        away_big_chances_missed as big_chances_missed,
        away_hit_woodwork as hit_woodwork,
        away_passes as passes,
        away_accurate_passes as accurate_passes,
        away_accurate_passes_pct as accurate_passes_pct,
        away_own_half as own_half,
        away_opposition_half as opposition_half,
        away_accurate_long_balls as accurate_long_balls,
        away_accurate_crosses as accurate_crosses,
        away_accurate_crosses_pct as accurate_crosses_pct,
        away_corners as corners,
        away_throws as throws,
        away_fouls_committed as fouls_committed,
        away_offsides as offsides,
        away_yellow_cards as yellow_cards,
        away_red_cards as red_cards,
        away_touches_in_opposition_box as touches_in_opposition_box,
        away_tackles_won as tackles_won,
        away_tackles_won_pct as tackles_won_pct,
        away_interceptions as interceptions,
        away_blocks as blocks,
        away_clearances as clearances,
        away_keeper_saves as keeper_saves,
        away_duels_won as duels_won,
        away_ground_duels_won as ground_duels_won,
        away_ground_duels_won_pct as ground_duels_won_pct,
        away_aerial_duels_won as aerial_duels_won,
        away_aerial_duels_won_pct as aerial_duels_won_pct,
        away_successful_dribbles as successful_dribbles,
        away_successful_dribbles_pct as successful_dribbles_pct,
        ingested_at
    from matches_base
),

unioned as (
    select * from home_side
    union all
    select * from away_side
)

select * from unioned